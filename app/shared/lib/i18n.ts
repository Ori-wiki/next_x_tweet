const dictionaries = {
  en,
  ru,
} satisfies Record<UserLanguage, Dictionary>;

export function getDictionary(language?: UserLanguage) {
  return dictionaries[language ?? DEFAULT_USER_SETTINGS.language];
}

export function resolveLanguage(settings?: UserSettings | null) {
  return settings?.language ?? DEFAULT_USER_SETTINGS.language;
}

export function formatMessage(template: string, values: Record<string, string>) {
  return Object.entries(values).reduce(
    (message, [key, value]) => message.replaceAll(`{${key}}`, value),
    template,
  );
}

